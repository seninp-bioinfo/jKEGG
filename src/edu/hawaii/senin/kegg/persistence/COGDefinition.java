package edu.hawaii.senin.kegg.persistence;

public class COGDefinition {

  private String cog_id;
  private String cog_class;
  private String description;

  public String getCog_id() {
    return cog_id;
  }

  public void setCog_id(String cog_id) {
    this.cog_id = cog_id;
  }

  public String getCog_class() {
    return cog_class;
  }

  public void setCog_class(String cog_class) {
    this.cog_class = cog_class;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

}
